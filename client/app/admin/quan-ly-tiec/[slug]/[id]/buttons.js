const exportComponentToPDF = (elementId, fileName) => {
    const input = document.getElementById(elementId); // Lấy phần tử dựa vào ID

    html2canvas(input, { scale: 2 }) // Chụp ảnh với độ phân giải cao
        .then((canvas) => {
            const imgData = canvas.toDataURL("image/png");
            const pdf = new jsPDF("p", "mm", "a4");
            
            // Căn chỉnh hình ảnh trong PDF theo kích thước A4
            const pdfWidth = pdf.internal.pageSize.getWidth();
            const pdfHeight = (canvas.height * pdfWidth) / canvas.width;
            
            pdf.addImage(imgData, "PNG", 0, 0, pdfWidth, pdfHeight);
            pdf.save(`${fileName}.pdf`);
        })
        .catch((error) => console.error("Lỗi khi xuất PDF:", error));
};

